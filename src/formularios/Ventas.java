    private void jButtonAgregarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAgregarProductoActionPerformed
 try {
        // Validar selecci√≥n de producto
        ServicioProducto productoSeleccionado = (ServicioProducto) jComboBoxProductos.getSelectedItem();
        if (productoSeleccionado == null) {
            JOptionPane.showMessageDialog(this, "‚ùå Debe seleccionar un producto", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Validar cantidad
        if (jTextFieldCantidadProducto.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "‚ùå Debe ingresar la cantidad", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int cantidad = Integer.parseInt(jTextFieldCantidadProducto.getText());
        if (cantidad <= 0) {
            JOptionPane.showMessageDialog(this, "‚ùå La cantidad debe ser mayor a 0", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Verificar stock
        if (cantidad > productoSeleccionado.getStock()) {
            JOptionPane.showMessageDialog(this, 
                "‚ùå Stock insuficiente. Stock disponible: " + productoSeleccionado.getStock(), 
                "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Agregar producto a la tabla
        Venta venta = new Venta();
        venta.agregarProductoATabla(
            jTableProductos,
            productoSeleccionado.getCodigoProducto(),
            productoSeleccionado.getNombre(),
            cantidad,
            productoSeleccionado.getPrecio()

        );

        // Actualizar total
        actualizarTotalVenta();
        
        // Limpiar campo de cantidad
        jTextFieldCantidadProducto.setText("");

    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "‚ùå La cantidad debe ser un n√∫mero v√°lido", "Error", JOptionPane.ERROR_MESSAGE);
    }
   
    }//GEN-LAST:event_jButtonAgregarProductoActionPerformed

    private void jButtonGenerarVentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGenerarVentaActionPerformed
 if (jTableProductos.getModel().getRowCount() == 0) {
    JOptionPane.showMessageDialog(this, "‚ùå Debe agregar al menos un producto", "Error", JOptionPane.ERROR_MESSAGE);
    return;
}

Cliente clienteSeleccionado = (Cliente) jComboBoxClientes.getSelectedItem();
if (clienteSeleccionado == null) {
    JOptionPane.showMessageDialog(this, "‚ùå Debe seleccionar un cliente", "Error", JOptionPane.ERROR_MESSAGE);
    return;
}

// Verificar si hay sesi√≥n activa (empleado logueado)
if (!Login.haySesionActiva()) {
    JOptionPane.showMessageDialog(this, "‚ö†Ô∏è No hay ning√∫n empleado con sesi√≥n activa.\nPor favor, inicie sesi√≥n nuevamente.", "Sesi√≥n no encontrada", JOptionPane.WARNING_MESSAGE);
    return;
}

try {
    // üîπ Obtener el empleado activo desde la sesi√≥n
    int codigoEmpleadoActivo = Login.getSesionActiva().getCodigoEmpleado();

    // üîπ Crear instancia de Venta
    Venta venta = new Venta();

    // üîπ Generar la venta con datos del formulario
    int codigoVentaGenerada = venta.generarVenta(
        clienteSeleccionado.getCodigoCliente(),
        codigoEmpleadoActivo, // ‚úÖ tomado de la sesi√≥n activa
        jTextFieldDireccionVenta.getText().trim(),
        jTextFieldTelefonoContacto.getText().trim(),
        jTableProductos
    );

    // üîπ Verificar resultado
    if (codigoVentaGenerada > 0) {
        JOptionPane.showMessageDialog(this, 
            "‚úÖ Venta registrada correctamente.\nC√≥digo de venta: " + codigoVentaGenerada,
            "√âxito", JOptionPane.INFORMATION_MESSAGE);
        
        limpiarFormulario(); // Limpia los campos del formulario
    }

} catch (Exception ex) {
    JOptionPane.showMessageDialog(this,
        "‚ùå Error al registrar la venta:\n" + ex.getMessage(),
        "Error", JOptionPane.ERROR_MESSAGE);
    ex.printStackTrace();
}
}

