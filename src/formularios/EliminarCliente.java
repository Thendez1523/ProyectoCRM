    private void jButtonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEliminarActionPerformed
         Cliente seleccionado = (Cliente) jComboBoxClientes.getSelectedItem();

    if (seleccionado != null) {

        String motivo = jTextPaneMotivo.getText().trim(); // txtMotivo es tu JTextPane

        if (motivo.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debes escribir un motivo para eliminar el cliente.");
            return;
        }

        int confirmacion = JOptionPane.showConfirmDialog(
            this,
            "¿Seguro que deseas eliminar al cliente?\n\n"
            + "Cliente: " + seleccionado.getNombre() + " " + seleccionado.getApellido() + "\n"
            + "Motivo: " + motivo,
            "Confirmar Eliminación",
            JOptionPane.YES_NO_OPTION
        );

        if (confirmacion == JOptionPane.YES_OPTION) {

            Cliente cli = new Cliente();
            cli.eliminarCliente(seleccionado.getCodigoCliente()); // Método que tú ya tienes

            JOptionPane.showMessageDialog(this, 
                "Cliente eliminado correctamente.\n\nMotivo:\n" + motivo);

            // Recargar ComboBox
            Cliente.comboCliente(jComboBoxClientes);

            // Limpiar campos
            
        }

    } else {
        JOptionPane.showMessageDialog(this, "Selecciona un cliente primero.");
    }
    }//GEN-LAST:event_jButtonEliminarActionPerformed

