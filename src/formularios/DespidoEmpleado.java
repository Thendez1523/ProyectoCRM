    private void jButtonDespedirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDespedirActionPerformed
         Empleado seleccionado = (Empleado) jComboBoxEmpleados.getSelectedItem();
    String motivo = jTextAreaMotivo.getText();

    if (seleccionado != null && !motivo.trim().isEmpty()) {
        int confirm = JOptionPane.showConfirmDialog(this, 
            "¬øSeguro que deseas despedir a " + seleccionado.getNombre() + " " + seleccionado.getApellido() + "?", 
            "Confirmar despido", 
            JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            Empleado.registrarDespido(seleccionado.getCodigoEmpleado(), motivo);
            Empleado.comboEmpleados(jComboBoxEmpleados); // üîÑ Actualiza el combo
        }
    } else {
        JOptionPane.showMessageDialog(this, 
            "‚ö†Ô∏è Debes ingresar un motivo antes de despedir al empleado.", 
            "Atenci√≥n", 
            JOptionPane.WARNING_MESSAGE);
    }
    }//GEN-LAST:event_jButtonDespedirActionPerformed

